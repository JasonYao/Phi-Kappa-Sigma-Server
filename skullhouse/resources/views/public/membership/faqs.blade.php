    <!-- Question 9 -->
    <div class="alert alert-success" role="alert">
    <strong>Question:</strong> What is rush week?
    </div>

    <div class="alert alert-info" role="alert">
    <strong>Answer:</strong> Rush Week, or simply “rush,” is a weeklong period every semester
      when each fraternity hosts events in order to meet potential new
      members (or “rushees”). The idea is for rushees to meet and mingle with
      the fraternities in order to find the group of men that suits them
      best, and then narrow their rushing efforts on the specific house they
      have the strongest affinity for.
    </div>

